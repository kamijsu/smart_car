//设置电荷泵是否使能
void oled_set_charge_pump(bool on) {
	//写设置电荷泵命令
	oled_write_cmd(0x8D);
	if (on) {
		//使能电荷泵
		oled_write_cmd(0x14);
	} else {
		//关闭电荷泵
		oled_write_cmd(0x10);
	}
}

