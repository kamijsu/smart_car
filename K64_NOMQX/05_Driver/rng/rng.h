//==========================================================================
//文件名称：rng.h
//功能概要：K64 RNG底层驱动程序头文件
//==========================================================================

#ifndef _RNG_H
#define _RNG_H

//包含公共头文件
#include "common.h"

//==========================================================================
//函数名称: rng_init
//函数返回: 无
//参数说明: 无
//功能概要: 初始化RNG模块
//==========================================================================
void rng_init();

//==========================================================================
//函数名称: rng_next_uint32
//函数返回: 32位随机数
//参数说明: 无
//功能概要: 生成一个32位随机数
//==========================================================================
uint32 rng_next_uint32();

//==========================================================================
//函数名称: rng_next_uint16
//函数返回: 16位随机数
//参数说明: 无
//功能概要: 生成一个16位随机数
//==========================================================================
uint16 rng_next_uint16();

//==========================================================================
//函数名称: rng_next_uint8
//函数返回: 8位随机数
//参数说明: 无
//功能概要: 生成一个8位随机数
//==========================================================================
uint8 rng_next_uint8();

//==========================================================================
//函数名称: rng_next_bool
//函数返回: true或false
//参数说明: 无
//功能概要: 生成true或false
//==========================================================================
bool rng_next_bool();

//==========================================================================
//函数名称: rng_next_bytes
//函数返回: 无
//参数说明: bytes:存储随机字节数组的首地址
//         len:随机字节数组的长度
//功能概要: 生成一定长度的随机字节数组
//==========================================================================
void rng_next_bytes(uint8* bytes, uint32 len);

#endif
